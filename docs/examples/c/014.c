#include <stdio.h>

int main()
{

    int nc;

    nc = 0;

    printf("the value of nc is %d\n", nc);
    printf("the value of nc is %d\n", ++nc);
    printf("the value of nc is %d\n", nc++);
    printf("the value of nc is %d\n", nc);

    printf("\n");

    printf("the value of nc is %d\n", nc);
    printf("the value of nc is %d\n", --nc);
    printf("the value of nc is %d\n", nc--);
    printf("the value of nc is %d\n", nc);

}
